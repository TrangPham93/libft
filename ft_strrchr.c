char	*ft_strrchr(const char *s, int c)
{
	const char	*p;

	p = ((void *)0);
	while (*s != '\0')
	{
		if (*s == c)
			p = s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return ((char *)p);
}
